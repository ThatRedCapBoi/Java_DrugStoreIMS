    private void loadCategoriesIntoCombo() {
        try {
            cmbCategory.removeAllItems();
            categories = categoryController.listCategories();

            for (Category c : categories) {
                cmbCategory.addItem(c.getName());
            }

            if (categories.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "No categories found. Please create a category first.",
                        "Warning",
                        JOptionPane.WARNING_MESSAGE);
                btnSave.setEnabled(false);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Failed to load categories:\n" + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            btnSave.setEnabled(false);
        }
    }

    private void fillForm(Product p) {
        txtSku.setText(p.getSku());
        txtName.setText(p.getName());
        txtPrice.setText(p.getPrice() == null ? "" : p.getPrice().toPlainString());
        spnQty.setValue(p.getQuantity());
        for (int i = 0; i < categories.size(); i++) {
            if (categories.get(i).getId() == p.getCategoryId()) {
                cmbCategory.setSelectedIndex(i);
                break;
            }
        }
    }
    private long selectedCategoryId() {
        int idx = cmbCategory.getSelectedIndex();
        if (idx < 0 || idx >= categories.size()) {
            return -1;
        }
        return categories.get(idx).getId();
    }

