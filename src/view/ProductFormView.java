    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Product p = (editing != null) ? editing : new Product();

            p.setSku(txtSku.getText());
            p.setName(txtName.getText());

            // Price parsing
            String priceText = txtPrice.getText().trim();
            p.setPrice(new BigDecimal(priceText));

            p.setQuantity((Integer) spnQty.getValue());

            long catId = selectedCategoryId();
            p.setCategoryId(catId);

            if (editing == null) {
                productController.create(p, role);
            } else {
                productController.update(p, role);
            }

            if (onSaved != null) {
                onSaved.run();
            }
            dispose();

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this,
                    "Invalid price format. Example: 12.50",
                    "Validation",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

